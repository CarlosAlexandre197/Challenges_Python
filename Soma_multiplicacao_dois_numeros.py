'''Faça um programa que leia dois números e faça a soma e a multiplicação entre eles.'''

n1 = int(input('Digite um número: '))
n2 = int(input('Digite outro número: '))
resultado_soma = n1 + n2
resultado_multiplicacao = n1 * n2 
print('A soma é: {} e a multiplicação é: {}'.format(resultado_soma, resultado_multiplicacao))
